package smiteTroll.Classes;

public class God {

    private String godName;
    private String godType;

    public God(String godName, String godType) {
        this.godName = godName;
        this.godType = godType;
    }

    public String getGodName() {
        return godName;
    }

    public String getGodType() {
        return godType;
    }

}
